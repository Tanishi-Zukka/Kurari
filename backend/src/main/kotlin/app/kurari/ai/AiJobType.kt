package app.kurari.ai

/** ジョブの出力形式。json のときは instruction でJSONのみの出力を強制する */
enum class ResponseFormat { text, json }

/**
 * AIジョブ種別ごとの instruction（システムプロンプト）を backend が持つ。
 * instruction はジョブ作成時に payload へ同梱され、Agent は
 * instruction + context + prompt を結合して実行するだけ（種別を知らない）。
 */
enum class AiJobType(val instruction: String, val responseFormat: ResponseFormat) {

    summarize_board(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すのは、あるボード上の付箋とコメントの一覧です。
内容を簡潔に日本語で要約してください。論点・決定事項・未解決の疑問があれば分けて示してください。
コードの編集やコマンド実行は不要です。テキストで回答だけを返してください。""",
        ResponseFormat.text,
    ),

    summarize_selection(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すのは、ボード上でユーザーが選択した要素の一覧です。
選択された要素の内容を300字以内の日本語で要約してください。
要約は付箋1枚に収まる密度にし、前置きや見出しは付けず本文のみを返してください。""",
        ResponseFormat.text,
    ),

    brainstorm(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すボードの内容を踏まえて、ユーザーの指示に沿ったアイデアを付箋の文面として提案してください。
出力はJSON配列のみ。各要素はアイデア1件の文字列（50字以内）。
コードフェンス・前置き・後書きは一切禁止。例: ["アイデア1","アイデア2","アイデア3"]
指定された件数ちょうどを返してください。""",
        ResponseFormat.json,
    ),

    summarize_document(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すのは、あるドキュメントの本文です。
内容を簡潔に日本語で要約してください。重要な決定事項・未解決の疑問があれば分けて示してください。
テキストで回答だけを返してください。""",
        ResponseFormat.text,
    ),

    draft_document(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すのは、あるドキュメントの現在の本文です。
ユーザーの指示に沿って、このドキュメントに追記する下書きをMarkdownで書いてください。
見出しは ## 以下を使い、既存本文の繰り返しは避けてください。Markdown本文のみを返してください。""",
        ResponseFormat.text,
    ),

    summarize_transcript(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すのは、会話や口述の音声を文字起こししたテキストです（誤変換を含む場合があります）。
内容を日本語で整理して要約してください。決定事項・TODO・未解決の疑問があれば分けて示してください。
テキストで回答だけを返してください。""",
        ResponseFormat.text,
    ),

    call_minutes(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すのは通話の文字起こしです（誤変換を含む場合があります）。
日本語のMarkdownで議事録ドラフトを作成してください。
「# 議事録」を先頭に置き、参加者、決定事項、TODO、議論の要点を見出しと箇条書きで整理してください。
文字起こしにない事実は補わず、Markdown本文のみを返してください。""",
        ResponseFormat.text,
    ),

    call_live_summary(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すのは進行中の通話の、ここまでの文字起こしです（誤変換を含む場合があります）。
ここまでの内容の要点を日本語で3〜7件に整理してください。
出力はJSONオブジェクトのみ: {"points":["要点1","要点2"]}。各要点は60字以内。
文字起こしにない事実は補わないでください。コードフェンス・前置き・後書きは一切禁止。""",
        ResponseFormat.json,
    ),

    project_brief(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すのは、あるプロジェクトのボード・ドキュメント・チャットの内容一式です。
このプロジェクトに新しく参加したメンバー向けに、プロジェクトの目的・現状・主要な論点を
日本語で分かりやすく説明してください。テキストで回答だけを返してください。""",
        ResponseFormat.text,
    ),

    detect_conflicts(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すプロジェクトの内容から、記述同士の矛盾・食い違い（ボードとドキュメントで方針が違う、
決定と逆の記述がある等）を検出してください。
出力はJSON配列のみ。各要素は {"topic":"論点","a":"記述A","b":"矛盾する記述B","hint":"解消のヒント"}。
矛盾がなければ [] を返す。コードフェンス・前置き・後書きは一切禁止。""",
        ResponseFormat.json,
    ),

    extract_decisions(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すプロジェクトの内容から、確定済みの意思決定と、未解決の事項を抽出してください。
出力はJSONオブジェクトのみ: {"decisions":["決定1",…],"openQuestions":["未解決1",…]}。
コードフェンス・前置き・後書きは一切禁止。""",
        ResponseFormat.json,
    ),

    chat_reply(
        """あなたはチームのコンテキスト管理アプリ「Kurari」のAIアシスタントです。
以下に渡すワークスペースの内容と直近の会話を踏まえて、ユーザーの質問・指示に日本語で答えてください。
簡潔に、必要なら箇条書きで。コンテキストにない事実を断定しないでください。
テキストで回答だけを返してください。""",
        ResponseFormat.text,
    );

    companion object {
        fun parse(s: String): AiJobType? = entries.find { it.name == s }
    }
}
